    internal static class FontAweSomeV6ItemBuilder
    {
        internal static void Build()
        {
            for (int i = 1; i < 4; i++)
            {
                string file = "D:\\Untitled-" + i + ".html";
                StringBuilder sb = new StringBuilder();
                string[] strings = File.ReadAllLines(file);

                foreach (string s in strings)
                {
                    string tmp = s;
                    int idx = tmp.IndexOf("<i class=\"fa");

                    while (idx > -1)
                    {
                        tmp = tmp.Substring(idx);
                        idx = tmp.IndexOf(">");
                        string key = tmp.Substring(0, idx);
                        key = key.Replace("<i class=\"fa-solid ", "");
                        key = key.Replace("<i class=\"fa-regular ", "");
                        key = key.Replace("<i class=\"fa-brands ", "");
                        key = key.Replace(" fa-fw\"", "");
                        sb.Append(key);
                        sb.Append(Environment.NewLine);
                        tmp = tmp.Substring(idx);
                        idx = tmp.IndexOf("<i class=\"fa");
                    }
                }

                File.WriteAllText("D:\\line" + i + ".txt", sb.ToString());
            }

            for (int i = 1; i < 4; i++)
            {
                string file = "D:\\line" + i + ".txt";
                StringBuilder sb = new StringBuilder();
                string[] strings = File.ReadAllLines(file);

                string all = File.ReadAllText("D:\\all.css");
                foreach (string s in strings)
                {
                    int idx = all.IndexOf(s + ":");
                    if (idx > -1)
                    {
                        int idx1 = all.IndexOf("\\", idx);
                        int idx2 = all.IndexOf("\"", idx1);
                        string tmp = all.Substring(idx1 + 1, idx2 - idx1 - 1);
                        sb.Append("public const int " + s.Replace("-", "_") + " = 0x" + tmp + ";");
                        sb.Append(Environment.NewLine);
                    }
                    else
                    {
                        Console.WriteLine(s);
                    }
                }

                File.WriteAllText("D:\\Out" + i + ".txt", sb.ToString());
            }

        }
    }
